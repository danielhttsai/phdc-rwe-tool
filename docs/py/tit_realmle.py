"""The PUBLISHED trend-in-trend estimator — the exact cell-count maximum-likelihood
estimator of Ji, Small, Leonard & Hennessy (2017, Epidemiology), as implemented in the
CRAN package ``TrendInTrend::OR()``. Re-implemented from scratch in numpy/scipy (no package
source copied; the package is cited as the reference).

Why this lives in its own module, behind a button: the cell-MLE has 3 + 3·K parameters and
needs a multi-start optimiser, so a faithful fit takes ~15 s — too slow to run live in the
browser without freezing it. It is also only identified on data generated by the Ji & Small
mechanism (exposure Z ~ Bernoulli(individual-CPE × time-factor)), and is genuinely finicky
(a single sample can land well off the truth, with wide CIs and a "weak identification" flag).
So we PRE-COMPUTE its result offline on a fixed Ji & Small-regime synthetic dataset and serve
the baked numbers instantly; ``_recompute()`` reproduces them for audit. The fast, always-on
③ estimate uses the lighter marginal model in ``tit_core`` — same identifying idea.

Model (subject-specific, Ji et al. 2017):
    P(Y=1 | Z=1, g, t) = exp(b0 + b1 + b2·t) · C1[g] / C3[g]
    P(Y=1 | Z=0, g, t) = exp(b0 + b2·t) · (C2[g] − C1[g]·h2[t]) / (1 − C3[g]·h2[t])
b1 = log causal odds ratio (OR = exp(b1)); C1/C2/C3[g] per-stratum latent CPE moments;
h2[t] the recursive marginal exposure-prevalence factor. Fitted by the binomial cell
log-likelihood with the analytic gradient, multi-start (Nelder–Mead → Newton root → Nelder–Mead).
"""
from __future__ import annotations

import numpy as np
from scipy import optimize

from i18n import t

G = 5
TN = 20
N = 100000
TRUE_OR = 2.5            # the published estimator's canonical demo effect (Ji & Small)


# ---------------------------------------------------------------------------
# Ji & Small generative model (a vaccine-framed port of TrendInTrend::GenData)
# Returns G×TN cell-count matrices n11/n10/n01/n00 and the true h2[t].
# ---------------------------------------------------------------------------
def gen_cells(seed=1):
    rng = np.random.default_rng(seed)
    a0, a2 = -13.0, 0.9
    a1 = np.array([1.0, 0.5, 0.5, 0.5, 0.5])
    x = np.column_stack([
        rng.normal(2, 1, N), rng.normal(2, 1, N),
        rng.binomial(1, 0.8, N), rng.binomial(1, 0.2, N), rng.binomial(1, 0.1, N),
    ]).astype(float)
    h2 = np.empty(TN)
    for tt in range(1, 16):
        h2[tt - 1] = np.exp(a2 * tt - 0.5 / 16 * tt ** 2) / 10000
    for tt in range(16, 21):
        h2[tt - 1] = np.exp(1.8 + a2 * tt - 0.6 / 15.7 * tt ** 2) / 10000
    h1 = np.exp(a0 + x @ a1) * 10000
    h1 = np.minimum(h1, 1.0 / h2.max())
    order = np.argsort(h1)
    x = x[order]; h1 = np.sort(h1)
    Z = np.zeros((N, TN))
    for tt in range(TN):
        Z[:, tt] = rng.binomial(1, np.clip(h1 * h2[tt], 0, 1))
    b0, b1, b2 = -4.0, np.log(TRUE_OR), 0.001
    b3 = 0.1 * np.array([1, 0.5, 0.1, 0.1, 0.1])
    C1 = np.empty(G); C2 = np.empty(G); C3 = np.empty(G)
    for i in range(G):
        sl = slice(i * (N // G), (i + 1) * (N // G))
        C1[i] = np.mean(np.exp(x[sl] @ b3) * h1[sl])
        C2[i] = np.mean(np.exp(x[sl] @ b3))
        C3[i] = np.mean(h1[sl])
    n11 = np.zeros((G, TN)); n10 = np.zeros((G, TN))
    n01 = np.zeros((G, TN)); n00 = np.zeros((G, TN))
    for i in range(G):
        sl = slice(i * (N // G), (i + 1) * (N // G))
        for j in range(TN):
            n1 = int(Z[sl, j].sum()); n0 = N // G - n1
            p1 = np.exp(b0 + b1 + b2 * (j + 1)) * C1[i] / C3[i]
            p0 = np.exp(b0 + b2 * (j + 1)) * (C2[i] - C1[i] * h2[j]) / (1 - C3[i] * h2[j])
            n11[i, j] = rng.binomial(n1, min(max(p1, 0), 1)); n10[i, j] = n1 - n11[i, j]
            n01[i, j] = rng.binomial(n0, min(max(p0, 0), 1)); n00[i, j] = n0 - n01[i, j]
    return n11, n10, n01, n00


# ---------------------------------------------------------------------------
# Cell-count likelihood + analytic gradient (translated from TrendInTrend LL/grr)
# t is the 1-based period index (1..TN). h2 recomputed from the cells (OR_opt recursion).
# ---------------------------------------------------------------------------
def _h2_from_cells(n11, n10, n01, n00):
    exposed = (n11 + n10).sum(0)
    total = (n11 + n10 + n01 + n00).sum(0)
    h2 = np.empty(TN); h2[0] = 0.001
    for k in range(1, TN):
        h2[k] = h2[k - 1] * (exposed[k] / max(total[k], 1e-9)) * (total[k - 1] / max(exposed[k - 1], 1e-9))
    return h2


def _flatten(n11, n10, n01, n00, h2):
    g = np.repeat(np.arange(G), TN)
    tc = np.tile(np.arange(1, TN + 1), G).astype(float)
    h2c = np.tile(h2, G)
    return g, tc, h2c, n11.ravel(), n10.ravel(), n01.ravel(), n00.ravel()


def _probs(params, g, tc, h2c, K):
    b0, b1, b2 = params[0], params[1], params[2]
    C1 = np.clip(params[3:3 + K], 1e-9, None)[g]
    C2 = params[3 + K:3 + 2 * K][g]
    C3 = np.clip(params[3 + 2 * K:3 + 3 * K], 1e-9, None)[g]
    C2 = np.maximum(C2, C1 * h2c + 1e-9)
    C3 = np.minimum(C3, (1 - 1e-9) / np.maximum(h2c, 1e-12))
    e1 = np.exp(b0 + b1 + b2 * tc); e0 = np.exp(b0 + b2 * tc)
    p1 = np.clip(e1 * C1 / C3, 1e-12, 1 - 1e-12)
    p0 = np.clip(e0 * (C2 - C1 * h2c) / (1 - C3 * h2c), 1e-12, 1 - 1e-12)
    return C1, C2, C3, e1, e0, p1, p0


def _neg_ll(params, A, g, tc, h2c, K):
    _, _, _, _, _, p1, p0 = _probs(params, g, tc, h2c, K)
    n11, n10, n01, n00 = A
    ll = n11 * np.log(p1) + n10 * np.log(1 - p1) + n01 * np.log(p0) + n00 * np.log(1 - p0)
    v = float(np.sum(ll))
    return -v if np.isfinite(v) else 1e18


def _neg_grad(params, A, g, tc, h2c, K):
    C1, C2, C3, e1, e0, p1, p0 = _probs(params, g, tc, h2c, K)
    n11, n10, n01, n00 = A
    a1 = n10 * p1 / (1 - p1); a0 = n00 * p0 / (1 - p0)
    grad = np.zeros(params.size)
    grad[0] = np.sum(n11 - a1 + n01 - a0)
    grad[1] = np.sum(n11 - a1)
    grad[2] = np.sum(tc * (n11 - a1 + n01 - a0))
    dC1 = n11 / C1 - n10 * e1 / C3 / (1 - p1) - n01 * h2c / (C2 - h2c * C1) \
        + n00 * e0 * h2c / (1 - h2c * C3) / (1 - p0)
    dC2 = n01 / (C2 - h2c * C1) - n00 * e0 / (1 - h2c * C3) / (1 - p0)
    dC3 = -n11 / C3 + n10 * (C1 * e1) / C3 ** 2 / (1 - p1) + n01 * h2c / (1 - h2c * C3) \
        + n00 / (1 - p0) * (-e0 * (C2 - h2c * C1) * h2c / (1 - h2c * C3) ** 2)
    for gi in range(K):
        m = (g == gi)
        grad[3 + gi] = np.sum(dC1[m]); grad[3 + K + gi] = np.sum(dC2[m]); grad[3 + 2 * K + gi] = np.sum(dC3[m])
    return -grad


def _num_hess_11(f, x, eps=1e-4):
    """Second derivative of f w.r.t. beta1 (index 1) only — for a Wald SE on b1."""
    xp = x.copy(); xp[1] += eps
    xm = x.copy(); xm[1] -= eps
    return (f(xp) - 2 * f(x) + f(xm)) / (eps * eps)


def fit_or(n11, n10, n01, n00, K=5, n_explore=12, seed=7):
    h2 = _h2_from_cells(n11, n10, n01, n00)
    g, tc, h2c, c11, c10, c01, c00 = _flatten(n11, n10, n01, n00, h2)
    A = (c11, c10, c01, c00)
    nll = lambda p: _neg_ll(p, A, g, tc, h2c, K)
    ngr = lambda p: _neg_grad(p, A, g, tc, h2c, K)
    nm = lambda s: optimize.minimize(nll, s, method="Nelder-Mead",
                                     options={"maxiter": 6000, "xatol": 1e-7, "fatol": 1e-8})
    rng = np.random.default_rng(seed)
    start = np.concatenate([[-10.0, 0, 0], np.full(K, 0.5), np.full(K, 1.0), np.full(K, 0.2)])
    best = None
    for it in range(n_explore):
        r1 = nm(start); s = r1.x.copy()
        try:
            sol = optimize.root(ngr, s, method="hybr", options={"maxfev": 4000})
            s = sol.x if abs(sol.x[1]) <= 50 else np.concatenate([[-10.0, 0, 0], s[3:]])
        except Exception:
            pass
        r2 = nm(s)
        if best is None or r2.fun < best.fun:
            best = r2
        start = best.x + rng.normal(0, np.array([1, 1, 0.5] + [0.2] * (3 * K))) / np.sqrt(it + 1)
    b1 = float(best.x[1])
    try:
        d2 = _num_hess_11(nll, best.x)
        se = float(np.sqrt(1.0 / d2)) if d2 > 0 else float("nan")
    except Exception:
        se = float("nan")
    # weak-identification heuristic (Ji & Small flag a fit whose CI fails to cover b1)
    not_identified = (not np.isfinite(se)) or se > 1.0 or abs(b1) > 5
    return b1, se, bool(best.success), float(-best.fun), bool(not_identified)


def naive_or_from_cells(n11, n10, n01, n00):
    a = n11.sum(); b = n10.sum(); c = n01.sum(); d = n00.sum()
    return float((a * d) / (b * c)) if b and c else float("nan")


def trend_curves(n11, n10, n01, n00, strata=(0, 4)):
    """Exposure & outcome prevalence over period for a low- and a high-CPE stratum."""
    N = n11 + n10 + n01 + n00
    out = {"periods": list(range(1, TN + 1)), "exposure": [], "outcome": []}
    for gi in strata:
        p = ((n11[gi] + n10[gi]) / N[gi]).tolist()
        q = ((n11[gi] + n01[gi]) / N[gi]).tolist()
        out["exposure"].append({"g": gi, "p": [round(v, 4) for v in p]})
        out["outcome"].append({"g": gi, "q": [round(v, 5) for v in q]})
    return out


def _recompute(seed=1):
    """Offline: generate Ji & Small-regime cells and fit the real cell-MLE. Used to
    produce the baked _REAL below and by the audit test."""
    n11, n10, n01, n00 = gen_cells(seed=seed)
    b1, se, conv, ll, ni = fit_or(n11, n10, n01, n00)
    or_hat = float(np.exp(b1))
    lo = float(np.exp(b1 - 1.96 * se)) if np.isfinite(se) else None
    hi = float(np.exp(b1 + 1.96 * se)) if np.isfinite(se) else None
    return {
        "or": round(or_hat, 3), "ci": [round(lo, 3) if lo else None, round(hi, 3) if hi else None],
        "true_or": TRUE_OR, "naive_or": round(naive_or_from_cells(n11, n10, n01, n00), 3),
        "beta1": round(b1, 4), "loglik": round(ll, 1), "not_identified": ni,
        "curves": trend_curves(n11, n10, n01, n00),
        "n": N, "periods": TN, "strata": G,
    }


